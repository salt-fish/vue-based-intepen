<template>
  <div :class="className" :style="{height:height,width:width}"></div>
</template>

<script>
import echarts from 'echarts'
require('echarts/theme/macarons') // echarts theme
import { debounce } from '@/utils'

export default {
  props: {
    className: {
      type: String,
      default: 'chart'
    },
    width: {
      type: String,
      default: '100%'
    },
    height: {
      type: String,
      default: '350px'
    },
    autoResize: {
      type: Boolean,
      default: true
    },
    chartData: {
      type: Object
    }
  },
  data() {
    return {
      chart: null
    }
  },
  mounted() {
    setTimeout(this.initChart, 1000)
    if (this.autoResize) {
      this.__resizeHanlder = debounce(() => {
        if (this.chart) {
          this.chart.resize()
        }
      }, 100)
      window.addEventListener('resize', this.__resizeHanlder)
    }

    // 监听侧边栏的变化
    const sidebarElm = document.getElementsByClassName('sidebar-container')[0]
    sidebarElm.addEventListener('transitionend', this.__resizeHanlder)
  },
  beforeDestroy() {
    if (!this.chart) {
      return
    }
    if (this.autoResize) {
      window.removeEventListener('resize', this.__resizeHanlder)
    }

    const sidebarElm = document.getElementsByClassName('sidebar-container')[0]
    sidebarElm.removeEventListener('transitionend', this.__resizeHanlder)

    this.chart.dispose()
    this.chart = null
  },
  watch: {
    chartData: {
      deep: true,
      handler(val) {
        this.setOptions(val)
      }
    }
  },
  methods: {
    setOptions(data) {
      const seriesData = []
      for (let i = 0; i < data.type.length; i++) {
        if (data.type.length === 1) {
          seriesData.push({
            name: data.type[i],
            smooth: true,
            type: 'line',
            data: data['number'],
            animationDuration: 2800,
            animationEasing: 'quadraticOut'
          })
        } else {
          seriesData.push({
            name: data.type[i],
            smooth: true,
            type: 'line',
            data: data[data.name[i]],
            animationDuration: 2800,
            animationEasing: 'quadraticOut'
          })
        }
      }

      this.chart.setOption({
        xAxis: {
          data: data.date,
          boundaryGap: false,
          axisTick: {
            show: false
          }
        },
        grid: {
          left: '3%',
          right: '4%',
          bottom: 20,
          top: 30,
          containLabel: true
        },
        dataZoom: [{
          startValue: '2018-01-15'
        }, {
          type: 'inside'
        }],
        tooltip: {
          trigger: 'axis',
          axisPointer: {
            type: 'cross'
          },
          padding: [5, 10]
        },
        yAxis: {
          axisTick: {
            show: false
          }
        },
        legend: {
          data: data.type
        },
        series: seriesData
      }, { notMerge: true })
    },
    initChart() {
      this.chart = echarts.init(this.$el, 'macarons')
      this.setOptions(this.chartData)
    }
  }
}
</script>
